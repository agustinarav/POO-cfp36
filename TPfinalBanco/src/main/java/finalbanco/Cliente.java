    private void CREARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CREARActionPerformed
       
                 //Generador de txt 
                
                 String nombreT = nombreC.getText (); 
                 String apellidoT = apellidoC.getText (); 
                 String cuitT = cuitC.getText(); 
                 String domiT = domiC.getText(); 
                 String fechaNacT = fechaC.getText(); 
                 String hijT = hijC.getText (); 
                 String mailT = mailC.getText(); 
                 String locT = locC.getText(); 
                 String genT = genC.getSelectedItem().toString();
                 String provT = provC.getSelectedItem().toString();
                 String estadoT = estadoC.getSelectedItem().toString();
                 
                 
                 
                 try

                {



                FileWriter archivoCliente = new FileWriter ( "ArchivoCliente.txt", true); 
 
                archivoCliente.write ( "\t \n Nombre: " + nombreT + 
                                        " Apellido: " + apellidoT + 
                                        " Fecha de nacimiento: " +  fechaNacT + 
                                        " Género " + genT +
                                        " Cuit: "+ cuitT + 
                                        " Domicilio: " + domiT + 
                                        " Localidad: " + locT +
                                        " Provincia " + provT + 
                                        " Estado Civil " + estadoT +
                                        " Hijos: " +  hijT +
                                        " Mail: " + mailT   ) ;     
                                       
                  

                JOptionPane.showMessageDialog( null , "El archivo fue creado con éxito", "Mensaje", JOptionPane.INFORMATION_MESSAGE);


                archivoCliente.close();





                }

                catch ( Exception e )

                {


                 JOptionPane.showInternalMessageDialog( null , "Error");



                } 


        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_CREARActionPerformed

    private void hijCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_hijCKeyTyped
       
        
        
        if ( hijC.getText().matches ("[0-9]*") )
            {validacionC.setText( "" ) ; }
        
               
        
        else { validacionC.setText( "Ingrese un número" );                              
                hijC.setText("")  ; } 
        
        
        
        
        
    }//GEN-LAST:event_hijCKeyTyped

