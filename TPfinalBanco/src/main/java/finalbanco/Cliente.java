    private void mailCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mailCKeyTyped
       
        
        
        if ( mailC.getText().contains ( "@" )  ) { validacionC.setText( "");  }
        
        else { validacionC.setText( " Inrgese mail válido ");  }
        
        
        if ( mailC.getText().contains ( "." ) ) { validacionC.setText( "") ;  }
        
        else { validacionC.setText( " Inrgese mail válido ");  }
        
        
        
        
        
    }//GEN-LAST:event_mailCKeyTyped


